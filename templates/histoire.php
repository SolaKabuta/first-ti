        <p>
            Le nom de la ville est dû selon la tradition à la légende des jumeaux Romulus et Rémus, où le premier tua le second, obtenant ainsi le droit de donner son nom à la ville qu'il construisait. Une autre hypothèse suggère que le nom de Rome viendrait du mot Rumon, nom étrusque du Tibre, qui serait ainsi devenue « La ville du fleuve ». La date historique de la fondation de la ville est le 21 avril de l'an 753 av. J.-C. (date qui a été retenue comme celle du début du calendrier romain).
        </p>

        <p>
            Surnoms de Rome : La Ville aux Sept Collines, La Ville Éternelle, Caput mundi (la capitale du monde).
            Les symboles de Rome sont :
            la louve capitoline, inspirée par la légende de la fondation de Rome ;
            SPQR (le Sénat et le peuple romain) : la devise antique, qui est toujours celle de la ville de Rome : elle fait partie du blason de la ville, et figure toujours sur les édifices publics, bâtiments et bouches d'égout.
            Les couleurs de Rome sont le rouge et le jaune (la pourpre et l'or).
        </p>

        <p>
            Origine légendaire <br> <br>

            La fondation de Rome est légendaire ; elle est attribuée à Romulus, descendant d'Enée, qui aurait été élevé par une louve, avec son frère Rémus. La tradition populaire généralement admise – et reprise aussi par Varron (qui dans le De lingua latina jeta les bases de l'étude linguistique du peuple latin) – veut qu'elle ait été fondée par Romulus le 21 avril 753 av. J.-C. ; après avoir tué son frère. Dans ses Annales, Ennius situe la fondation en 875, tandis que Fabius Quintus se rapproche de la position de Varron la situant en 748. Déterminer la date exacte de la naissance de Rome n'a jamais été une tâche facile pour les historiographes, Lucius Cincius Alimentus (auteur d'écrits en forme d'annales) et l’historien grec Timée de Tauroménion (qui vécut environ au troisième siècle av. J.-C.) plaçaient respectivement en 729 et en 814 la fondation de la future « ville éternelle » (pour Timée, donc, elle est presque contemporaine de celle de Carthage).
        </p>

        <p>
            D'un point de vue scientifique, on admet que Rome est née de l'association des tribus de la région (et de la réunion des villages qui occupaient le sommet des collines) vers le viiie siècle av. J.-C. ; il faut y voir une décision stratégique et politique plutôt que la construction d'une cité ex nihilo. Ainsi commença la période de la « Rome carrée », ainsi nommée d'après la forme carrée du mont Palatin, même si on ne peut pas encore parler d'une véritable ville. Le développement de cette implantation commence sous la domination étrusque. Les fouilles menées autour du Forum Boarium (Marché aux bœufs) et du Forum Romanum (Forum de Rome) démontrent une occupation d'abord relativement fruste, puis une influence reliée à la culture étrusque, notamment de nombreux fragments de céramique architectonique, contemporaine d'une évolution dans les modes funéraires. Au cours du viiie siècle av. J.-C., l'urbanisation gagne les grands centres italiens, formant un chapelet de cités commerciales, unies par les échanges mais non fédérées. L'enjeu stratégique de la future Rome explique en grande partie la domination progressive de la culture étrusque dans cet embryon de ville. Rapidement, la société se diversifie, la population croît, et la monarchie s'impose.
        </p>

        <p>
            Romulus sera le premier roi de Rome et six autres monarques lui succéderont, eux aussi probablement d’origine étrusque. À chaque souverain, on attribue généralement une contribution particulière dans la naissance et la création des institutions romaines et dans le développement socio-politique de la cité : Romulus est considéré comme le fondateur de la ville, à laquelle il donna ses principales institutions civiles et le Sénat, Numa Pompilius créa les principales institutions religieuses (parmi lesquelles le temple de Janus, le culte des vestales, la charge de Grand Pontife - pontifex maximus - la subdivision de l’année en douze mois avec, précisément réglementées, toutes les fêtes et célébrations religieuses), Tullus Hostilius, en battant les Sabins et en conquérant Alba Longa, amorça l'expansion territoriale dans le Latium, à Ancus Marcius on doit la fondation du port de Rome par excellence, Ostie, Tarquin l'Ancien érigea le temple de Jupiter et construisit le cloaca maxima, Servius Tullius divisa la population citadine en cinq classes de cens et construisit la première enceinte fortifiée (les murailles serviennes dont on peut observer quelques vestiges à l'intérieur du Forum Termini) ; le dernier roi fut Tarquin le Superbe qui, à cause de son comportement arrogant et de son mépris envers les citoyens et les institutions romaines, sera chassé par le peuple en 509 av. J.-C.
        </p>

        <p>
            La déposition du dernier roi de Rome coïncida avec une période de fort déclin des Étrusques : ceux-ci en effet, dans leur expansion vers le sud, arrivèrent au contact des Grecs. À la suite d'un premier conflit avec les colons commença la décadence. Rome réussit ainsi à se libérer du joug étrusque, après avoir chassé Tarquin le Superbe. Les Étrusques laissèrent une influence durable sur Rome. Les Romains apprirent d'eux à construire des temples, et leur doivent l'introduction du culte d’une triade de dieux (Junon, Minerve et Jupiter) : Uni, Menrva et Tinia. Ils transformèrent Rome d'une communauté de bergers en une ville. Ils servirent encore d'intermédiaires dans la transmission d'éléments empruntés à la culture grecque, notamment une version occidentale de l’alphabet grec.
        </p>

        <p>

            République romaine <br> <br>

            Après 509 av. J.-C., Rome s'allia avec d'autres villes latines pour se défendre contre les Sabins. Avant 400 av. J.-C., la puissance étrusque était confinée à l'Étrurie. Rome émergea comme la ville dominante du Latium, mais en 387 av. J.-C., elle fut mise à sac par des Gaulois conduits par Brennos. Par la suite, Rome conquit l'Étrurie, prit des territoires aux Gaulois et soumit les Latins et les Samnites. En 290 av. J.-C., plus de la moitié de la péninsule était dominée par Rome, et au milieu du IIIe siècle av. J.-C., même les cités grecques furent soumises.
        </p>

        <p>
            Les guerres contre les cités italiennes, les Gaulois, les guerres puniques et celles contre la Macédoine consolidèrent la domination romaine sur l'Italie et entamèrent l'expansion en Espagne et en Macédoine. En 146 av. J.-C., Carthage et Corinthe furent détruites, transformant Rome en superpuissance méditerranéenne. Les classes dirigeantes s'ouvrirent à la culture grecque, et les grandes puissances hellénistiques furent battues ou vassalisées.
        </p>

        <p>
            L'expansion rapide de Rome créa des problèmes administratifs et sociaux. Les institutions romaines, conçues pour une cité-État, devaient désormais gérer des provinces étendues. Les guerres continuelles augmentèrent le nombre d'esclaves, utilisés dans les exploitations agricoles des patriciens, ce qui mit en crise les petits propriétaires terriens et créa un sous-prolétariat urbain.
        </p>

        <p>
            Le contact avec la civilisation grecque et l'arrivée de nombreux esclaves helléniques causèrent des sentiments ambivalents chez les Romains. D'une part, ils voulaient rénover les mœurs rurales romaines en introduisant des connaissances orientales, mais cela engendra aussi une « décadence » des valeurs morales traditionnelles. Cette opposition entre conservatisme et progressisme perdura dans l’histoire romaine.
        </p>

        <p>
            Au Ier siècle av. J.-C., la République commença à se fissurer. Les personnages influents, affirmant leur pouvoir personnel, conduisirent à la guerre civile. La République dut également affronter une révolte des esclaves menée par Spartacus, écrasée au prix de nombreux massacres. D'autres conquêtes suivirent, notamment la Gaule par Jules César, et les Romains allèrent jusqu'en Syrie et en Arménie.
        </p>

        <p>
            Histoire médiévale <br> <br>

            La chute de l'Empire romain d'Occident n'affecta pas immédiatement Rome, qui continua d'être administrée par le Sénat et le Pape, souvent issu de familles sénatoriales. Cette situation perdura jusqu'à ce que l'Empire romain d'Orient, sous Justinien Ier, prenne la ville en 536. Les guerres entre Ostrogoths et Byzantins dévastèrent Rome, qui fut plusieurs fois prise et reprise, tombant en décadence.
        </p>

        <p>
            Justinien Ier tenta de maintenir Rome en subventionnant l'entretien des infrastructures et en soutenant l'éducation. Cependant, le pouvoir local passa progressivement aux mains du Pape, plus influent que les autorités byzantines. Les invasions lombardes sous Alboïn réduisirent encore le contrôle byzantin, limitant leur pouvoir à quelques territoires côtiers.
        </p>

        <p>
            L'empereur Maurice s'allia avec Childebert II d'Austrasie pour contrer les Lombards, mais Rome souffrit d'inondations et de pestes. Le pape Grégoire Ier négocia un traité de paix avec les Lombards, qui fut reconnu par Maurice. Sous Phocas, la primauté du Pape sur l'Église fut reconnue.
        </p>

        <p>
            Au VIIe siècle, l'influence byzantine se renforça, mais les tensions théologiques, notamment sur le monothélisme, persistèrent. Le pape Martin Ier fut déporté et exilé en Crimée. En 663, l'empereur Constant II visita Rome, mais son voyage fut un échec en raison des différences culturelles et du manque de soutien concret.
        </p>

        <p>
            En 727, le pape Grégoire II refusa l'iconoclasme imposé par Léon III, qui confisqua les domaines du Pape en Sicile. Rome, désormais exclue de l'Empire byzantin, se tourna vers les Francs pour sa protection. En 753, le pape Étienne II sollicita Pépin le Bref pour attaquer les Lombards.
        </p>

        <p>
            Le 23 août 846, une flotte barbaresque pilla Rome, incitant le pape Léon IV à construire un mur défensif, la cité léonine, financé par l'empereur Lothaire Ier. En 849, une nouvelle flotte musulmane fut détruite par une tempête et une flotte chrétienne.
        </p>

        <p>
            La Rome des papes et de la Renaissance <br> <br>

            Les historiens évoquent un échange entre les milieux pontificaux et les Francs, où les premiers concédaient aux seconds le charisme impérial en échange de la reconnaissance de la « donation de Constantin ». Le pouvoir croissant du pape l'amena à affronter l'Empire byzantin, culminant dans la controverse sur l’iconoclastie. Rome, exclue de l'Empire byzantin, s'allia aux Francs, constituant ainsi les États pontificaux avec Rome comme capitale.
        </p>

        <p>
            À la Renaissance, Rome devint un important foyer culturel, rivalisant avec Florence et Venise. En 756, Pépin III fit de Rome la capitale des États pontificaux. Le gouvernement de la ville était disputé entre la noblesse romaine, le pape, le Saint-Empire romain germanique et des insurrections républicaines. Après 1434, la papauté soumit le gouvernement de Rome à la bureaucratie ecclésiastique.
        </p>

        <p>
            Au XVIe siècle, malgré le sac de 1527, Rome se transforma avec de nombreuses constructions. En 1555, le pape Paul IV créa le ghetto de Rome, regroupant la population juive dans des conditions difficiles.
        </p>

        <p>
            Histoire contemporaine <br> <br>

            À la fin du XVIIIe et au XIXe siècle, Rome a été marquée par des mouvements révolutionnaires. Le gouvernement des papes fut interrompu par la République romaine (1798) inspirée de la Révolution française. Rome fut aussi chef-lieu du département de Rome durant l'Empire français (1809-1814).
        </p>

        <p>
            En 1849, une autre République romaine émergea, soutenue par Giuseppe Mazzini et Giuseppe Garibaldi. Le pape s'opposa à l'unification italienne, mais en 1870, après la défaite française face à la Prusse, l'armée italienne prit Rome, annexant Rome et le Latium au royaume d'Italie.
        </p>

        <p>
            Le pape Pie IX refusa de conserver la Ville léonine, se déclarant prisonnier au Vatican. En 1871, Rome devint officiellement la capitale de l'Italie. La ville, alors peu développée, manquait de bourgeoisie libérale et d'industrie moderne.
        </p>

        <p>
            En 1874, la municipalité interdit la course de chevaux libres du Carnaval de Rome, contribuant à la disparition de cette fête. Entre 1870 et 1900, la population de Rome doubla, marquant son entrée dans la modernité.
        </p>

        <p>
            Situation actuelle <br> <br>

            Aujourd'hui, la population de Rome s'est stabilisée à 2,8 millions d'habitants, et son aire urbaine en compte environ 4 millions. C'est la troisième agglomération d'Italie après Milan et Naples, mais la commune elle-même est la plus peuplée du pays et la quatrième de l'Union Européenne. <br> <br>

            Des différences sociologiques existent selon les quartiers. Le nord (Tor di Quinto), le centre et le sud de la ville (EUR) sont aisés, tandis que la majeure partie des quartiers est (dont San Basilio et Alessandrino) sont populaires, tout comme Primavalle à l'ouest. <br> <br>

            Durant sa longue histoire, Rome a toujours eu une population caractérisée par d'importants flux migratoires. Par tradition, un « vrai » Romain est une personne dont la famille a vécu à Rome depuis au moins sept générations. Aujourd'hui, on considère qu'un individu né à Rome de parents nés à Rome est un « Romain de Rome ». <br> <br>

            La Rome d’aujourd’hui reflète les stratifications de sa longue histoire, mais c'est aussi une grande métropole moderne. Le vaste centre historique contient de nombreux vestiges de la Rome antique, des trésors artistiques de la Renaissance, des églises et des palais baroques, ainsi que des exemples de l’Art nouveau, du néoclassicisme, du modernisme et d’autres styles artistiques des XIXe et XXe siècles. <br> <br>

            Le centre historique s’identifie avec les limites des antiques murailles impériales. Certaines zones ont été restructurées à la suite de la réunification (1880-1910 Rome Umbertina), et quelques adjonctions furent réalisées pendant la période fasciste, avec la création de la Via dei Fori Imperiali et de la Via della Conciliazione face au Vatican. <br> <br>

            Durant la Seconde Guerre mondiale, Rome a souffert de lourds bombardements et de batailles, mais évita une destruction totale. La ville tomba aux mains des Alliés le 4 juin 1944. <br> <br>

            Après la guerre, Rome continua à s'étendre avec la création de nouveaux quartiers et faubourgs. La population actuelle est officiellement d'environ 2,8 millions d'habitants, mais les jours ouvrables, on estime qu'elle dépasse 3,5 millions. Rome accueillit les Jeux olympiques d'été de 1960, utilisant de nombreux sites antiques et créant de nouvelles structures comme le Stadio Olimpico. <br> <br>

            Beaucoup de monuments de Rome ont été restructurés par l’État italien et par le Vatican à l'occasion du Jubilé de 2000. Capitale de l’Italie, Rome accueille les principales institutions de la nation et de nombreuses institutions internationales. <br> <br>

            Aux élections municipales du 19 juin 2016, les Romains élisent pour la première fois une femme à la tête de leur ville : Virginia Raggi (du Mouvement 5 étoiles). <br> <br>

            Aujourd’hui, Rome est l’une des plus importantes destinations touristiques du monde, grâce à son immense patrimoine archéologique et à ses trésors artistiques, mais aussi pour ses traditions uniques et la beauté de ses vues et de ses villas.
        </p>
